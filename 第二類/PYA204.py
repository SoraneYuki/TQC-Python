a = int(input())
b = int(input())
c = input()

if c == "+":

    print("%d" % (a + b))

elif c == "-":

    print("%d" % (a - b))

elif c == "*":

    print("%d" % (a * b))

elif c == "/":

    print("%d" % (a / b))

elif c == "//":

    print("%d" % (a // b))

elif c == "%":

    print("%d" % (a % b))